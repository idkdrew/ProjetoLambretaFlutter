class Result {
  final int id;
  final int idTeam;
  final bool isHome;
  final int idTeamOpponent;
  final int homeGoal;
  final int awayGoal;

  Result({
    required this.id,
    required this.idTeam,
    required this.isHome,
    required this.idTeamOpponent,
    required this.homeGoal,
    required this.awayGoal,
  });
}
